    public void cargarProductos() throws SQLException {

        productos = new DefaultTableModel();
        productos.addColumn("Codigo");
        productos.addColumn("Descripcion");
        productos.addColumn("Existencia");
        productos.addColumn("Precio");

        ResultSet datos = CT.select("*", "\"schPracticaExamen\".articulo", "");

        while (datos.next()) {

            String[] valores = {datos.getString("codigo"), datos.getString("descripcion"), datos.getString("existencia"), datos.getString("precio")};
            productos.addRow(valores);

        }

        tblProductos.setModel(productos);

    }

    public void menu() {

        menu = new JPopupMenu();
        borrar = new JMenuItem("Eliminar");
        modificar = new JMenuItem("Modificar");

        modificar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {

                String estado;

                txtCodigo.setEnabled(false);
                txtCodigo.setText((String) tblProductos.getValueAt(tblProductos.getSelectedRow(), 0));
                txtDescripcion.setText((String) tblProductos.getValueAt(tblProductos.getSelectedRow(), 1));
                txtExistencia.setText((String) tblProductos.getValueAt(tblProductos.getSelectedColumn(), 2));
                txtPrecio.setText((String) tblProductos.getValueAt(tblProductos.getSelectedColumn(), 3));

                btnProceso.setText("Guardar");

            }
        });

        borrar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {

                String estado;

                estado = CT.eliminar("\"schPracticaExamen\".articulo", " where codigo='"
                        + tblProductos.getValueAt(tblProductos.getSelectedRow(), 0) + "'");
                try {
                    cargarProductos();
                } catch (SQLException ex) {
                    Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        });

        menu.add(borrar);
        menu.add(modificar);
        tblProductos.setComponentPopupMenu(menu);

    }

