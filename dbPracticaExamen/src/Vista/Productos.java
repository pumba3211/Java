    private void btnProcesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesoActionPerformed

        String datos;
        String estado;

        if ("Insertar".equals(btnProceso.getText())) {

            datos = "('" + txtCodigo.getText() + "','" + txtDescripcion.getText() + "','" + txtExistencia.getText() + "'"
                    + ",'" + txtPrecio.getText() + "')";
            estado = CT.Insertar("\"schPracticaExamen\".articulo", datos);
            try {
                cargarProductos();
            } catch (SQLException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtCodigo.setText("");
            txtDescripcion.setText("");

        } else {

            estado = CT.Modificar("\"schPracticaExamen\".articulo", "set descripcion='" + txtDescripcion.getText() + "', existencia='" + txtExistencia.getText() + "',"
                    + " precio='" + txtPrecio.getText() + "'", "where codigo='" + txtCodigo.getText() + "'");
            try {
                cargarProductos();
            } catch (SQLException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            }
            btnProceso.setText("Insertar");
            txtCodigo.setEnabled(true);
            txtCodigo.setText("");
            txtDescripcion.setText("");
            txtPrecio.setText("");
            txtExistencia.setText("");

        }

    }//GEN-LAST:event_btnProcesoActionPerformed

