public class aeropuertos {

    private String nombre;
    private String Pais;

    public aeropuertos() {
        this.nombre = "";
        this.Pais = "";
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPais() {
        return Pais;
    }

    public void setPais(String Pais) {
        this.Pais = Pais;
    }

}
