    public void cargarDatos() {
        ResultSet datos = conexioncitas.select4((String) tablaConsultas.getValueAt(tablaConsultas.getSelectedRow(), 0));

        try {
            while (datos.next()) {
                String[] valores = {datos.getString("cod_mascota"), datos.getString("nombre_mascota"), datos.getString("cod_enfermedad"),
                    datos.getString("nombre_enfermedad"), datos.getString("fecha_diagnostico"), datos.getString("notas"), datos.getString("cod_vacuna"),
                    datos.getString("nombre_vacuna"), datos.getString("fecha_aplicacion"), datos.getString("fecha_proxima_aplicacion")};
                datosconsulta.addRow(valores);
            }

        } catch (Exception e) {
            System.out.println("Error: " + e.toString());
        }
        tablaDatosDeconsulta.setModel(datosconsulta);
    }
