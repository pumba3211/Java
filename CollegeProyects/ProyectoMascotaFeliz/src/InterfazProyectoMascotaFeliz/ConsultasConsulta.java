    public void cargarConsultas() {
        tablaconsultas.addColumn("Codigo");
        tablaconsultas.addColumn("Codigo Cita");
        tablaconsultas.addColumn("Fecha Consulta");

        ResultSet datos = conexioncitas.select("*", "\"schProyecto\".\"consulta\"", "");

        try {
            while (datos.next()) {
                String[] valores = {datos.getString("cod"), datos.getString("cod_cita"), datos.getString("fecha_consulta")};
                tablaconsultas.addRow(valores);
            }

        } catch (Exception e) {
            System.out.println("Error: " + e.toString());
        }
        tablaConsultas.setModel(tablaconsultas);
    }

