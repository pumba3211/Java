    public ArrayList<Clientes> obtenerClientes() throws SQLException {

        ResultSet datos = CT.select("*", "\"schPractica\".\"cliente\"", "");
        ArrayList<Clientes> clientes = new ArrayList();
        while (datos.next()) {

            Clientes cli = new Clientes(datos.getString("descripcion"), datos.getString("codigo"));
            clientes.add(cli);

        }

        return clientes;

    }

    public void CargarClientes() throws SQLException {
        for (int x = 0; x < obtenerClientes().size(); x++) {
            ComBoxListaClientes.addItem(obtenerClientes().get(x).getNombre());
        }
    }

    public ArrayList<Productos> obtenerProductos() throws SQLException {
        ResultSet datos = CT.select("*", "\"schPractica\".\"articulo\"", "");
        ArrayList<Productos> productos = new ArrayList();
        while (datos.next()) {
            Productos pro = new Productos(datos.getString("descripcion"), datos.getString("codigo"), datos.getString("precio"));
            productos.add(pro);
        }
        return productos;

    }

    public void CargarProductos() throws SQLException {
        for (int x = 0; x < obtenerProductos().size(); x++) {
            ComBoxListaProductos.addItem(obtenerProductos().get(x).getDescripcion());
        }
    }

